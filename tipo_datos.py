#type --> funcion para concoer el tipo de dato
print(type(2.0))#Float

print(type('palabra'))#String

print(type(False))#booleano

print(type(10 == 10))#booleano

print(type(3 + 5)) #Int

print(type([5,7,65,67])) #list

print(type((10,25,68))) #Tupla

print(type({   #Diccionario
    "name":"alfonso",
    "lastname": "Ballesteros",
    "nickname":"elAlfonso",
    "celphone":31717526
}))